    @Override
    public void actionPerformed(ActionEvent e) {
        if((e.getSource() instanceof JButton)
                &&(((JButton)e.getSource()).equals(jButton1))) {
            User user = new User();
            user.setUsername(jTextField1.getText());
            user.setPassword(new String(jPasswordField1.getPassword()));

            UserDAO ud = new UserDAO();
            if(ud.checkLogin(user)) {
                if(user.getRole().equalsIgnoreCase("thu thu")) {
                    (new LibrarianHomeFrm(user)).setVisible(true);
                    this.dispose();
                }
                else 
                    JOptionPane.showMessageDialog(this, 
                                 "The function of the role " + user.getRole()
                                 + " is under construction!");
            }else {
                JOptionPane.showMessageDialog(this, 
                          "Incorrect username and/or password!");
            }
        }
    }
