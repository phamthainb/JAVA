    private void showData() {
        // conbox
        jComboBox3.removeAllItems();
        jComboBox2.removeAllItems();
        //
        tMH.setRowCount(0);
        for (MatHang matHang : lsMh) {
            jComboBox2.addItem(String.valueOf(matHang.getMa()));
            tMH.addRow(new Object[]{matHang.getMa(), matHang.getTen(), matHang.getGiaMua(), matHang.getGiaBan(), matHang.getNhom()});
        }
        //
        tNV.setRowCount(0);
        for (NhanVien i : lsNV) {
            jComboBox3.addItem(String.valueOf(i.getMa()));
            tNV.addRow(new Object[]{i.getMa(), i.getTen(), i.getSdt(), i.getSdt()});
        }
        //
        tDS.setRowCount(0);
        for (DanhSach i : lsDS) {
            tDS.addRow(new Object[]{i.getNv().getMa(), i.getNv().getTen(), i.getMh().getTen(), i.getSoLuong()});
        }
    }

    NhanVien getNV(int ma) {
        for (NhanVien i : lsNV) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    MatHang getMH(int ma) {
        for (MatHang i : lsMh) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    void sortNV() {
        lsDS.sort((o1, o2) -> {
            return o1.getNv().getTen().compareTo(o2.getNv().getTen());
        });
    }

    void sortMH() {
        lsDS.sort((o1, o2) -> {
            return o1.getMh().getTen().compareTo(o2.getMh().getTen());
        });
    }

