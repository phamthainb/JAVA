    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ten = jTextField1.getText(),
                giaMua = jTextField2.getText(),
                giaBan = jTextField3.getText();
        String nhom = (String) jComboBox1.getSelectedItem();

        String check = MatHang.check(giaMua, giaBan, ten, nhom);
        try {
            if (check.isEmpty()) {
                // add to ls, file, showd
                lsMh.add(new MatHang(Integer.parseInt(giaMua), Integer.parseInt(giaBan), ten, nhom));
                ct.write(lsMh, pMH);
                showData();
            } else {
                JOptionPane.showMessageDialog(rootPane, check);
                throw new HandleExp(check);
            }
        } catch (HandleExp ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

